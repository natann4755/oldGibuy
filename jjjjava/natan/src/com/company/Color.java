package com.company;

public class Color {
    private int red;
    private int blue;
    private int grin;

    public int getRed() {
        return red;
    }

    public void setRed(int red) {
        this.red = red;
    }

    public int getBlue() {
        return blue;
    }

    public void setBlue(int blue) {
        this.blue = blue;
    }

    public int getGrin() {
        return grin;
    }

    public void setGrin(int grin) {
        this.grin = grin;
    }
}
