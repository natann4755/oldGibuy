public class Toy {
   private String name;
   private String color;

   public Toy(String name, String color) {
      setName(name);
      setColor(color);
   }

   public String getName() {
      return name;
   }

   public void setName(String _name) {
      name = _name;
   }

   public String getColor() {
      return color;
   }

   public void setColor(String _color) {
      color = _color;
   }

   public int getM_age() {
      return 8;
   }
}
